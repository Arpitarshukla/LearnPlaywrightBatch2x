// Compound Assignment Operators 
// used in automation to update the value of a variable based on its current value.


let x = 10;


x += 10; // x = x + 10 compound assignment operator
console.log(x); // 20

x -= 3; // x = x - 3 compound assignment operator
console.log(x); // 17

x *= 2; // x = x * 2 compound assignment operator
console.log(x); // 34

x /= 17; // x = x / 17 compound assignment operator
console.log(x); // 2

x %= 2; // x = x % 2 compound assignment operator
console.log(x); // 0


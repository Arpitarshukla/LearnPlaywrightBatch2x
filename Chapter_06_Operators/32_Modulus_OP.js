// % = Modulus Operator
// a%b It returns the remainder of a division operation


let result = 13 % 7;
console.log(result); // Output: 6

console.log(101 % 2); // Output: 1 (since 101 is odd, it leaves a remainder of 1 when divided by 2)
console.log(100 % 2); // Output: 0 (since 100 is even, it leaves no remainder when divided by 2)
console.log(37 % 2); // Output: 1 (since 37 is odd, it leaves a remainder of 1 when divided by 2)
console.log(36 % 2); // Output: 0 (since 36 is even, it leaves no remainder when divided by 2)


//  n%2==1,  n=Odd number and n%2==0,  n=Even number